<?php
$n = 1;Объявляет переменную $n типа integer и инициализирует её значением 1
$sum = 0;Объявляет переменную $sum типа integer и инициализирует её значением 0
while ($n <= 15) { Цикл while выполняется, пока $n меньше или равно 15
    if ($n % 3 == 0) $sum += $n;Если $n делится на 3 без остатка, добавляет $n к $sum
    $n++;Увеличивает значение $n на 1 после каждой итерации
}
echo $sum;Выводит результат первой части: сумму чисел, кратных 3, от 1 до 15

$n = 3;Переопределяет переменную $n типа integer значением 3
$sum = 0;Сбрасывает переменную $sum в 0
do {
    $sum += $n;Добавляет текущее значение $n к переменной $sum
    $n += 3;Увеличивает значение $n на 3 после каждой итерации
} while ($n <= 15);Цикл do-while выполняется, пока $n меньше или равно 15
echo "<BR> $sum";Выводит HTML-тег переноса строки и результат второй части
?>