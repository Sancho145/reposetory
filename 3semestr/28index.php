<?php
// PHP-код начинается с открывающего тега.

$a = 3;Объявляет переменную $a типа integer и присваивает ей значение 3
$b = '3';Объявляет строковую переменную $b и присваивает ей значение '3' (строка)
$d = '3a';Объявляет строковую переменную $d и присваивает ей значение '3a' (строка)

var_dump($a < $b);Сравнивает $a и $b (3 < '3') с приведением типов
var_dump($a > $b);Сравнивает $a и $b (3 > '3') с приведением типов
var_dump($a == $d);Сравнивает $a и $d (3 == '3a') с приведением типов
var_dump($d == $b);Сравнивает $d и $b ('3a' == '3') с приведением типов
var_dump($a = !$b);Присваивает $a результат логического отрицания $b
var_dump($a = !$d);Присваивает $a результат логического отрицания $d
var_dump($a != $a);Проверяет, не равно ли $a самому себе
var_dump($b != $b);Проверяет, не равно ли $b самому себе
var_dump($d != $d);Проверяет, не равно ли $d самому себе

?>